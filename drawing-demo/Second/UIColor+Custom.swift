import UIKit

extension UIColor {
    
    static var darkBlack: UIColor {
        return UIColor(hex: 0x212121)
    }
    
    static var darkRed: UIColor {
        return UIColor(hex: 0x941100)
    }
    
    static var darkBlue: UIColor {
        return UIColor(hex: 0x005493)
    }
    
}

